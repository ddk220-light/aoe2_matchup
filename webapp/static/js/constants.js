/* --- Icon Helpers --- */
function getIconUrl(name) {
    const id = NAME_TO_ICON[name];
    if (!id) return null;
    return `${ICON_BASE}${id}.png`;
}

function getIconFallbackUrl(name) {
    const id = NAME_TO_ICON[name];
    if (!id) return null;
    return `${ICON_BASE_FALLBACK}${id}.png`;
}
